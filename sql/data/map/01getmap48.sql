SELECT OGR_FID  , 
LANDOFFICE_SEQ ,
UTMMAP1  , 
UTMMAP2 , 
UTMMAP3  , 
UTMMAP4 , 
UTMSCALE  , 
LAND_NO 
FROM MAP_.MAP_LAND_GIS_48 
WHERE RECORD_STATUS ='N'
AND (:OGR_FID  IS NULL OR OGR_FID = :OGR_FID)
AND (:LANDOFFICE_SEQ  IS NULL OR LANDOFFICE_SEQ = :LANDOFFICE_SEQ)
AND (:UTMMAP1  IS NULL OR UTMMAP1 = :UTMMAP1)
AND (:UTMMAP2  IS NULL OR UTMMAP2 = :UTMMAP2)
AND (:UTMMAP3  IS NULL OR UTMMAP3 = :UTMMAP3)
AND (:UTMMAP4  IS NULL OR LPAD(UTMMAP4,2,'00') = LPAD(:UTMMAP4,2,'00'))
AND (:UTMSCALE  IS NULL OR UTMSCALE = :UTMSCALE)
AND (:LAND_NO  IS NULL OR LAND_NO = :LAND_NO)