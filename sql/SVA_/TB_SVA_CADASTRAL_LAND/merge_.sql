        Source_.REQUEST_SEQ, Source_.PROCESS_SEQ, Source_.HOLD_LAND_SEQ, 
        Source_.CADASTRAL_LAND_STS_, 
        Source_.CADASTRAL_LAND_LOG_SEQ, 
        Source_.PROCESS_SEQ_, Source_.STATUS_SEQ_, 
        Source_.CADASTRAL_LAND_NOTE, 
        Source_.RECORD_STATUS, 
        Source_.CREATE_USER, Source_.CREATE_DTM--, Source_.LAST_UPD_USER, Source_.LAST_UPD_DTM 
    ) 

WHEN MATCHED THEN 
    UPDATE SET 
        -- Target_.CADASTRAL_LAND_SEQ = Source_.CADASTRAL_LAND_SEQ, 
        Target_.CADASTRAL_LAND_ORDER = Source_.CADASTRAL_LAND_ORDER, 
        Target_.CADASTRAL_LAND_REGDTM = TO_DATE( :CADASTRAL_LAND_REGDTM, 'YYYY-MM-DD' ), 
        Target_.CADASTRAL_LAND_REGDESC = Source_.CADASTRAL_LAND_REGDESC, 
        Target_.CADASTRAL_LAND_REGNAME = Source_.CADASTRAL_LAND_REGNAME, 
        Target_.CADASTRAL_LAND_PRINTED = Source_.CADASTRAL_LAND_PRINTED, 
        Target_.CADASTRAL_LAND_STS = Source_.CADASTRAL_LAND_STS, 
        Target_.CADASTRAL_LAND_CANCEL_DTM = Source_.CADASTRAL_LAND_CANCEL_DTM, 
        Target_.CADASTRAL_LAND_CANCEL_REM = Source_.CADASTRAL_LAND_CANCEL_REM, 
        Target_.PARCEL_SEQ = Source_.PARCEL_SEQ, 
        Target_.REGISTER_SEQ = Source_.REGISTER_SEQ, 
        Target_.REQUEST_SEQ = Source_.REQUEST_SEQ, 
        Target_.PROCESS_SEQ = Source_.PROCESS_SEQ, 
        Target_.HOLD_LAND_SEQ = Source_.HOLD_LAND_SEQ, 
        Target_.CADASTRAL_LAND_STS_ = Source_.CADASTRAL_LAND_STS_, 
        Target_.CADASTRAL_LAND_LOG_SEQ = Source_.CADASTRAL_LAND_LOG_SEQ, 
        Target_.PROCESS_SEQ_ = Source_.PROCESS_SEQ_, 
        Target_.STATUS_SEQ_ = Source_.STATUS_SEQ_, 
