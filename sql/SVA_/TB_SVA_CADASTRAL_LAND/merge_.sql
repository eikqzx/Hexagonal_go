        REQUEST_SEQ, PROCESS_SEQ, HOLD_LAND_SEQ, 
        CADASTRAL_LAND_STS_, 
        CADASTRAL_LAND_LOG_SEQ, 
        PROCESS_SEQ_, STATUS_SEQ_, 
        CADASTRAL_LAND_NOTE, 
        RECORD_STATUS, 
        CREATE_USER, CREATE_DTM--, LAST_UPD_USER, LAST_UPD_DTM 
    ) VALUES ( 
        SVA_.SQ_SVA_CADASTRAL_LAND_SEQ.NEXTVAL, 
        Source_.CADASTRAL_LAND_ORDER, Source_.CADASTRAL_LAND_REGDTM, 
        Source_.CADASTRAL_LAND_REGDESC, Source_.CADASTRAL_LAND_REGNAME, 
        Source_.CADASTRAL_LAND_PRINTED, Source_.CADASTRAL_LAND_STS, 
        Source_.CADASTRAL_LAND_CANCEL_DTM, Source_.CADASTRAL_LAND_CANCEL_REM, 
